package com.algoandroid.priyank.algoandroidapps;

/**
 * A class that will work as blueprint for chip
 */
public class Chip {

    private String first;
    private String second;

    public Chip(String first, String second) {
        this.first = first;
        this.second = second;
    }
    public String getSecond() {
        return second;
    }

}
